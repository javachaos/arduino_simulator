#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PointerRegister {
    X,
    Y,
    Z,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PointerMode {
    Direct,
    PostIncrement,
    PreDecrement,
}

