#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub struct OperandSet {
    pub d: Option<u8>,
    pub r: Option<u8>,
    pub a: Option<u8>,
    pub b: Option<u8>,
    pub s: Option<u8>,
    pub k: Option<i32>,
    pub q: Option<u8>,
    pub pointer: Option<PointerRegister>,
    pub mode: Option<PointerMode>,
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct DecodedInstruction {
    pub address: u32,
    pub opcode: u16,
    pub next_word: Option<u16>,
    pub mnemonic: Mnemonic,
    pub word_length: u8,
    pub operands: OperandSet,
}
